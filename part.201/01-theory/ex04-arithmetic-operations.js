// * — multiplication
console.log(10 * 2); // 20

// / — division
console.log(10 / 2); // 5
console.log(10 / 4); // 5

// - — subtraction

console.log(10 - 2); // 8

// % — modulo
console.log(10 % 2); // 0
console.log(7 % 3); // 1
console.log(15 % 4); // 3

// ** — exponentiation
console.log(10 ** 2); // 100
console.log((+10) ** 2); // 100

// - — inverting
console.log(-10); // -10

// operator precedence
console.log(10 ** (4 - 1));
console.log(10 ** 4 - 2);
console.log(2 + 2 * 2);
console.log((2 + -2) * 2);
console.log(2 - (-2));

// a = b · q + r,
// где a — делимое,
// b — делитель,
// q — неполное частное,
// r — остаток. 0 ⩽ r < | b |.

console.log(-20 % 7);
// 4 - 5
// -10